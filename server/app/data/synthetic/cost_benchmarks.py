# ── Merged Hospital Pool ───────────────────────────────────────────────────────

def get_all_hospitals() -> List[Dict[str, Any]]:
    """
    Return the full merged hospital pool:
      - 1 500 real hospitals from seeds (with enriched doctors + sentiment)
      - 13 curated synthetic hospitals (real named Indian hospitals with addresses)

    Deduplication key: lowercase(name + city).
    Synthetic records take priority — they carry richer procedure/specialist data.
    """
    try:
        from app.data.loader import REAL_HOSPITALS
        real = REAL_HOSPITALS
    except Exception:
        real = []

    # Build lookup of synthetic hospitals by dedup key
    synth_keys = {
        (h["name"].lower(), h["city"].lower())
        for h in SYNTHETIC_HOSPITALS
    }

    # Add real hospitals that don't collide with synthetic names
    combined: List[Dict[str, Any]] = list(SYNTHETIC_HOSPITALS)
    for h in real:
        key = (h["name"].lower(), h["city"].lower())
        if key not in synth_keys:
            combined.append(h)

    return combined
